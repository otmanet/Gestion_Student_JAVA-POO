    public void porstange(double a , double b){
        switch(sign){
            case "+":
               add(((a*b)/100), a);
               break;
            case "-":
               substract(((a*b)/100), a);
               break;
            case "*":
               multiply(((a*b)/100), a);
               break;
            case "/":
               divide(((a*b)/100), a);
               break;
        
        }
    }
