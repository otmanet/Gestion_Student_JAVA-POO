    private void egaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_egaleActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().length()>0){
            double b=Double.parseDouble(jTextField1.getText());
            switch(sign){
                case "+":
                    add(toBeCalcultedValue, b);
                    break;
                case "-":
                    substract(toBeCalcultedValue, b);
                    break;
                case "*":
                    multiply(toBeCalcultedValue, b);
                    break;
                case "/":
                    divide(toBeCalcultedValue, b);
                    break;
            }
        }
    }//GEN-LAST:event_egaleActionPerformed

